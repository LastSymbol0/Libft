#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char	*p1;
	char	*p2;

	p1 = dst;
	p2 = (char *)src;
	while (n-- > 0)
		*(p1 + n) = *(p2 + n);
	return (dst);
}
