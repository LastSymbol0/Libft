#include "libft.h"

int		ft_countwords(const char *s, char c)
{
	int		i;
	int		j;
	int		k;

	i = -1;
	k = 0;
	while (s[++i] != '\0')
	{
		j = i;
		while (s[j] != c && s[j] != '\0')
			j++;
		if (j - i > 0)
		{
			k++;
			i = j;
		}
	}
	return (k);
}
